// Primitve data types: number, boolean and string
// y would result in "undefined"
var x = 6, y = 12;
console.log( x );
console.log( y );
console.log( x * y );
console.log( x / y );
console.log( y / x );

console.log( typeof x );
console.log( typeof y ); // number

// boolean is a true or false

var IsItRaining = true; IsItPouring = false;
console.log( IsItRaining );
console.log( IsItPouring );
console.log( typeof IsItRaining );
console.log( typeof IsItPouring );

// string

var name = "John", message = 'Good morning';
console.log( name );
console.log( typeof message );
console.log( message.length ); // 12
console.log( message + " " + name );
console.log ( typeof name.length );

